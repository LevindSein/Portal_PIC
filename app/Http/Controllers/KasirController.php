    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if(request()->ajax()){
            $data = Tagihan::with('pengguna:id,name');

            return DataTables::of($data)
            ->addColumn('action', function($data){
                $button = '';
                $button .= '<button type="button" id="'.Crypt::encrypt($data->id).'" nama="'.$data->name.'" class="bayar btn btn-sm btn-success">Bayar</button>';
                return $button;
            })
            ->editColumn('pengguna.name', function($data){
                $name = $data->pengguna->name;
                if(strlen($name) > 15) {
                    $name = substr($name, 0, 11);
                    $name = str_pad($name,  15, ".");
                }

                return "<span data-toggle='tooltip' title='" . $data->pengguna->name . "'>$name</span>";
            })
            ->editColumn('tagihan', function($data){
                return number_format($data->tagihan->selisih, 0, ',', '.');
            })
            ->filterColumn('name', function ($query, $keyword) {
                $keywords = trim($keyword);
                $query->whereRaw("CONCAT(name, nicename) like ?", ["%{$keywords}%"]);
            })
            ->rawColumns(['action', 'name', 'tagihan', 'pengguna.name'])
            ->make(true);
        }
        return view('Layanan.Kasir.index');
    }

